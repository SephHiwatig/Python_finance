print(arr_plus_arr)

arr_times_arr = arr * arr
print(arr_times_arr)

arr_minus_arr = arr - arr
print(arr_minus_arr)

arr_divide_arr = arr / arr
print(arr_divide_arr)

arr_times_constant = arr * 100
print(arr_times_constant)

arr_plus_constant = arr + 100
print(arr_plus_constant)